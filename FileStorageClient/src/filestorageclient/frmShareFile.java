                            JOptionPane.showMessageDialog(frmShareFile.this, "L·ªói: Ng∆∞·ªùi d√πng '" + targetUsername + "' kh√¥ng t·ªìn t·∫°i.", "L·ªói", JOptionPane.ERROR_MESSAGE);
